    public Frame_CreateActivityReport(Form_ActivityReports formActivityReports, Home home) {
        this.formActivityReports = formActivityReports;
        this.home = home;
        initComponents();
        
        
        //Load DO List ComboBox
        deliveryorderDAO = new DeliveryOrderDAO(home);
        deliveryOrders = deliveryorderDAO.getDeliveryOrders();
        if (deliveryOrders != null && !deliveryOrders.isEmpty()) {
            DefaultComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>();
            for (DeliveryOrderModel deliveryOrder : deliveryOrders) {
                comboBoxModel.addElement(String.valueOf(deliveryOrder.getId()));
            }
            jComboDOId.setModel(comboBoxModel);
        } else {
            jComboDOId.setEnabled(false);
            JOptionPane.showMessageDialog(this, "Tidak ada DO yang tersedia.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
        
        jLabelId.setVisible(false);
    }

