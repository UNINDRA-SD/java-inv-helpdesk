    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        if (getjButtonSave().getText().equals("Save")){
            ActivityReportController activityReport = new ActivityReportController(formActivityReports, home);
            activityReport.saveActivityReport(this);
            activityReport.showActivityReports();
        }
        else {
            ActivityReportController activityReport = new ActivityReportController(formActivityReports, home);
            activityReport.updateActivityReport(this);
            activityReport.showActivityReports();
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

